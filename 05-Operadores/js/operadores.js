var num1 = 11
var num2 = '11'
var num3 = 26

// Operador Mayor que y mayor o igual que
console.log(num1 > num3 )
console.log(num2 >= num1 )

// Operador Menor que y menor o igual que
console.log(num1 < num3 )
console.log(num2 <= num1 )

// Operador igual
console.log(num1 == num2 ) //Solo revisa valor y no tipo de dato
console.log(num1 == num2 )

console.log(typeof num1)
console.log(typeof num2)

// Operador extrictamente igual que
console.log(num1 === num2 ) // Revisa valor y tipo de dato
console.log(num1 === num2 )


// Operador Diferente a 
console.log(num1 != num3 ) //Revisa si es diferente uno al otro
console.log(num1 != num2 ) // Revisa solo si el valor es diferente en este caso false
console.log(num1 !== num2 ) // Revisa si tanto el valor como el tipo son diferentes, en este caso true

